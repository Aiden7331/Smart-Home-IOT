package IOT;


public class MemoDTO {

	String LBody;
	String LetterList;
	String LWritter;
	String LHead;
	
	
	public void setLBody(String LBody) {
		this.LBody=LBody;
	}
	
	public void setLetterList(String LetterList) {
		this.LetterList=LetterList;
	}
	
	public void setLWritter(String LWritter) {
		this.LWritter=LWritter;
	}
	
	public void setLHead(String LHead) {
		this.LHead=LHead;
	}
	public String getLBody() {
		return LBody;
	}
	
	public String getLetterList() {
		return LetterList;
	}
	
	public String getLWritter() {
		return LWritter;
	}
	
	public String getLHead() {
		return LHead;
	}
}
